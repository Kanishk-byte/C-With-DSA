#include <iostream>
using namespace std;

int main()
{
    // * Boolean Data Type -> Only stores 2 values.
    // * Flag Variable usually used in Bool Data Type.
    // * If we give any integer except 0 then output is 1. Ex -> 7, 7.5, 0.3
    // * If we give 0 except any integer then output is 0. Ex -> 0

    // * 1) True = 1 -> On
    // * 2) False = 0 -> Off

    bool flag = true;
    cout << "Flag : " << flag << endl;

    bool var = false;
    cout << "Var : " << var << endl;

    // * cout<<(7>4); -> This expression is true so it return 1 in output.

    return 0;
}